    private void mainPanelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainPanelMousePressed

        if (g2 == null) {
            g2 = (Graphics2D) mainPanel.getGraphics();
        }
        xStart = evt.getX();
        yStart = evt.getY();
        xEnd = evt.getX();
        yEnd = evt.getY();

    }//GEN-LAST:event_mainPanelMousePressed

    private void mainPanelMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainPanelMouseReleased
        if (jRadioButtonLine.isSelected()) {
            mainPanel.update(g2);
            xEnd = evt.getX();
            yEnd = evt.getY();
            linebuffer = new Line2D.Float((float) xStart, (float) yStart, (float) xEnd, (float) yEnd);
            lineContainer.add(linebuffer);
            DrawAll();
        }

    }//GEN-LAST:event_mainPanelMouseReleased

    private void mainPanelMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainPanelMouseDragged

        if (jRadioButtonLine.isSelected()) {
            mainPanel.update(g2);
            xEnd = evt.getX();
            yEnd = evt.getY();
            linebuffer = new Line2D.Float((float) xStart, (float) yStart, (float) xEnd, (float) yEnd);
            DrawAll();
            g2.draw(linebuffer);
        }
    }//GEN-LAST:event_mainPanelMouseDragged

